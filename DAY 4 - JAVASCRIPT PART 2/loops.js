// for loop
console.log("--- FOR LOOP ---");
for (let i = 3; i <= 5; i++) {
  console.log("For loop: ", i);
}

// while loop
console.log("--- WHILE LOOP ---");
let count = 1;
while (count <= 5) {
  console.log("While loop: ", count);
  count++;
  console.log("While loop: ", count);
  console.log("----");
}

// do-while loop
console.log("--- DO-WHILE LOOP ---");
let counter = 1;
do {
  console.log("Do while count: ", counter);
  counter++;
} while (counter <= 5);
