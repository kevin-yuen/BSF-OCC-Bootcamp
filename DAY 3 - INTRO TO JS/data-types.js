// Primitive Data Types

// Numbers
let score = 58;
console.log("Type of Score: ", typeof score);
console.log("Score: ", score);

let decimalNumber = 5.8;
console.log("Type of decimalNumber: ", typeof decimalNumber);
console.log("decimalNumber: ", decimalNumber);

// String
let playerName = "Mario_123";
console.log("Type of Player Name: ", typeof playerName);
console.log("Player Name: ", playerName);

let number = "58";
console.log("Type of number: ", typeof number);
console.log("Number: ", number);

// Boolean
let isAdmin = true;
console.log("Type of isAdmin: ", typeof isAdmin);
console.log("isAdmin: ", isAdmin);

// Null - represent intentional absence of value
let car = null;
console.log("Type of Car ", typeof car);
console.log("Car: ", car);

// Undefined - represnt unintentional absence of value
let city;
console.log("Type of city: ", typeof city);
console.log("City: ", city);