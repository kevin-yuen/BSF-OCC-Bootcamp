let x = 11;
let y = 5;
let result;

// addition
result = x + y;
console.log("Sum of x and y: ", result);

// subtraction
result = x - y;
console.log("Difference of x and y: ", result);

// multiplication
result = x * y;
console.log("Product of x and y: ", result);

// division
result = x / y;
console.log("Quotient of x and y: ", result);

// remainder
result = x % y;
console.log("Remainder of x and y: ", result);

